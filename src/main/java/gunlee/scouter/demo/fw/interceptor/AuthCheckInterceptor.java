package gunlee.scouter.demo.fw.interceptor;

import gunlee.scouter.demo.commondemo.domain.User;
import lombok.extern.slf4j.Slf4j;
import org.slf4j.MDC;
import org.springframework.stereotype.Component;
import org.springframework.util.ObjectUtils;
import org.springframework.web.servlet.HandlerInterceptor;
import org.springframework.web.servlet.ModelAndView;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;

/**
 * @author Gun Lee (gunlee01@gmail.com) on 2018. 6. 17.
 */
@Component
@Slf4j
public class AuthCheckInterceptor implements HandlerInterceptor {

    @Override
    public boolean preHandle(HttpServletRequest request, HttpServletResponse response, Object handler)
            throws Exception {

        HttpSession session = request.getSession();
        User loginUser = (User) session.getAttribute("user");

        if(ObjectUtils.isEmpty(loginUser)){
            log.debug("[not logon user]");
            return true;
        }else{
            log.debug("[user] {}", loginUser.getUserId());
            MDC.put("userId", loginUser.getUserId());
            return true;
        }

    }

    @Override
    public void postHandle(HttpServletRequest request, HttpServletResponse response, Object handler,
                           ModelAndView modelAndView) throws Exception {
        // TODO Auto-generated method stub

    }

    @Override
    public void afterCompletion(HttpServletRequest request, HttpServletResponse response, Object handler, Exception ex)
            throws Exception {
        // TODO Auto-generated method stub

    }
}
